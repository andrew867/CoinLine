    public static ReadOnlySpan<ushort> Table => TableStorage.Data;

    public static ushort Compute(ReadOnlySpan<byte> data)
    {
        ushort crc = 0;
        foreach (var b in data)
            crc = Update(crc, b);
        return crc;
    }

    public static ushort Update(ushort crc, byte octet)
    {
        var c = crc & 0xFF;
        var high = (crc >> 8) & 0xFF;
        var idx = (byte)((c ^ octet) & 0xFF);
        var t = TableStorage.Data[idx];
        var tLo = t & 0xFF;
        var tHi = (t >> 8) & 0xFF;
        return (ushort)((tLo ^ high) | (tHi << 8));
    }

